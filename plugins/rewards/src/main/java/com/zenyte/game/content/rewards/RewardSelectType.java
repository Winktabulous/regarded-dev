package com.zenyte.game.content.rewards;

/**
 * Dictates how to select the items,
 * either random or select all in the entries list
 *
 * @author zackd on 24/06/2020
 */
public enum RewardSelectType {

    /**
     * Selects 1 random in the item entries list
     */
    RANDOM,

    /**
     * Selects all the entries in the list
     */
    ALL_ENTRIES

}